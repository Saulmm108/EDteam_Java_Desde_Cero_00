package Java_Desde_Cero_00.Estructuras_de_Control_Repetitivas_02;

public class Bucle_While_00 {
    public static void main(String[] args) {
        
        int counter = 0;

        while (counter <3){
            System.out.println("El contador es: "+counter);
            counter++;
        }

        System.out.println("Bucle finalizado");
    }
}
